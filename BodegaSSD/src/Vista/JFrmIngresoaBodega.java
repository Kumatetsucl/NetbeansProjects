    private void LlenarUsuario() {
        cboRutEmpleado.addItem("Seleccione");
        for (Empleado user : new DaoEmpleado().Listar()) {
            cboRutEmpleado.addItem(user.getRut());
            txtNombreEmpleado.setText(user.getNombre());
            
        }
    }

    private void Llenarconductor() {
        cboRutProveedor.addItem("Seleccione");
        for (Proveedor cond : new DaoProveedor().ListarConductor()) {
            cboRutProveedor.addItem(cond.getRut());
            txtNombreProveedor.setText(cond.getNombre());
            
        }
    }

    private void LlenarVehiculo() {
        cboProducto.addItem("Seleccione");
        for (Productos vehi : new DaoProductos().Listar()) {
            cboProducto.addItem(vehi.getChasisVehiculo());
            
        }
    }
    
    
    private void LlenarVehiculoFiltro() {
        cboProducto.addItem("Seleccione");
        for (Productos vehi : new DaoProductos().Listar()) {
            cboFiltroProveedor.addItem(vehi.getPatenteVehiculo());
        }
    }
    
    private void Listar() {
        DefaultTableModel modelo=new DefaultTableModel();
        JTblListarIngresos.setModel(modelo);
        modelo.addColumn("ID");
        modelo.addColumn("Cliente");
        modelo.addColumn("Conductor");
        modelo.addColumn("Vehiculo");
        modelo.addColumn("Inicio");
        modelo.addColumn("Fin");
        modelo.addColumn("IdaVuelta");
        modelo.addColumn("Fecha");
        ArrayList<IngresoBodega> listado=new DaoIngresoBodega().Listar();
        for (IngresoBodega item : listado) {
            Object[] fila=new Object[8];
            fila[0]=item.getId_viaje();
            fila[1]=item.getEmpleado().getNombre();
            fila[2]=item.getProveedor().getNombre();
            fila[3]=item.getProducto().getPatenteVehiculo();
            fila[4]=item.getDireccionInicio();
            fila[5]=item.getDireccionDestino();
            fila[6]=item.isIdaYVuelta()?"Si":"No";
            fila[7]=item.getFechaDeIngreso();
            modelo.addRow(fila);
        }
    }

    
    public void ListarFiltroPatente(){
        DefaultTableModel modelo = new DefaultTableModel();
        JTblListarIngresos.setModel(modelo);
        modelo.addColumn("ID");
        modelo.addColumn("Cliente");
        modelo.addColumn("Conductor");
        modelo.addColumn("Vehiculo");
        modelo.addColumn("Inicio");
        modelo.addColumn("Fin");
        modelo.addColumn("IdaVuelta");
        modelo.addColumn("Fecha");
        ArrayList<IngresoBodega> Listado = new DaoIngresoBodega().Listar();
        String pat = cboFiltroProveedor.getSelectedItem().toString();
        for (IngresoBodega item: Listado) {
            if(item.getProducto().getPatenteVehiculo().equals(pat)){
            Object[] fila=new Object[8];
            fila[0]=item.getId_viaje();
            fila[1]=item.getEmpleado().getNombre();
            fila[2]=item.getProveedor().getNombre();
            fila[3]=item.getProducto().getPatenteVehiculo();
            fila[4]=item.getDireccionInicio();
            fila[5]=item.getDireccionDestino();
            fila[6]=item.isIdaYVuelta()?"S":"N";
            fila[7]=item.getFechaDeIngreso().getTime();
            modelo.addRow(fila);
            }
        }
    }

