    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int fila=JTblListarIngresos.getSelectedRow();
        int idViaje=Integer.parseInt(JTblListarIngresos.getValueAt(fila, 0).toString());
        IngresoBodega via = new DaoIngresoBodega().Buscar2(idViaje);
        if (via != null) {
            JOptionPane.showMessageDialog(null, "Existe Viaje ");
            txtIdViaje.setText(""+via.getId_viaje());
            cboRutProveedor.setSelectedItem(via.getProveedor().getRut());
            cboRutEmpleado.setSelectedItem(via.getEmpleado().getRut());
            cboProducto.setSelectedItem(via.getProducto().getChasisVehiculo());
            txtDireccionInicio.setText(via.getDireccionInicio());
            txtDireccionDestino.setText(via.getDireccionDestino());
            lblVehiculo.setText(""+via.getProducto().getIdVehiculo());
            lblIdConductor.setText(""+via.getProveedor().getIdConductor());
            lblIdCliente.setText(""+via.getEmpleado().getIdCliente());
            

            if (via.isIdaYVuelta()==true){
                RdBtnSi.setSelected(true);
            }else{
                RdBtnSi.setSelected(false);
            }
            JdcFechaDeIngreso.setDate(via.getFechaDeIngreso());
        } else {
            JOptionPane.showMessageDialog(null, "No Existe Viaje");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

