    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int idViaje = Integer.parseInt(txtIdViaje.getText());
        
        String conductor= cboRutProveedor.getSelectedItem().toString();
        int idConductor = Integer.parseInt(lblIdConductor.getText());
        Proveedor c = new Proveedor();
        c.setRut(conductor);
        c.setIdConductor(idConductor);
        
        String usuario = cboRutEmpleado.getSelectedItem().toString();
        int idUsuario = Integer.parseInt(lblIdCliente.getText());
        Empleado u = new Empleado();
        u.setRut(usuario);
        u.setIdCliente(idUsuario);
        
        String vehiculo= cboProducto.getSelectedItem().toString();
        int idVehiculo = Integer.parseInt(lblVehiculo.getText());
        Productos v = new Productos();
        v.setChasisVehiculo(vehiculo);
        v.setIdVehiculo(idVehiculo);
        
        String DireccionInicio= txtDireccionInicio.getText();
        String DireccionDestino= txtDireccionDestino.getText();
        boolean IdaYVuelta;
        if (RdBtnSi.isSelected()){
            IdaYVuelta=true;
        }else{
            IdaYVuelta=false;
        }
        java.util.Date FechaDeViaje = JdcFechaDeIngreso.getDate();
        try {
            ////////////////////////////////////////////////////////
            IngresoBodega via = new IngresoBodega(idViaje, c, u, v, DireccionInicio, DireccionDestino, IdaYVuelta, FechaDeViaje);
            ////////////////////////////////////////////////////////
            boolean resp = new DaoIngresoBodega().Grabar(via);
            if (resp == true) {
                JOptionPane.showMessageDialog(null, "Grabo");
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
        Listar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error btn guardar: "+e.getMessage());
        } 
    }//GEN-LAST:event_btnGuardarActionPerformed

