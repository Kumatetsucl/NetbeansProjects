    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int idViaje = Integer.parseInt(txtIdViaje.getText());
        
        String conductor= cboRutConductor.getSelectedItem().toString();
        int idConductor = Integer.parseInt(lblIdConductor.getText());
        Conductor c = new Conductor();
        c.setRut(conductor);
        c.setIdConductor(idConductor);
        
        String usuario = cboRutUsuario.getSelectedItem().toString();
        int idUsuario = Integer.parseInt(lblIdCliente.getText());
        Usuario u = new Usuario();
        u.setRut(usuario);
        u.setIdCliente(idUsuario);
        
        String vehiculo= cboVehiculo.getSelectedItem().toString();
        int idVehiculo = Integer.parseInt(lblVehiculo.getText());
        Vehiculo v = new Vehiculo();
        v.setChasisVehiculo(vehiculo);
        v.setIdVehiculo(idVehiculo);
        
        String DireccionInicio= txtDireccionInicio.getText();
        String DireccionDestino= txtDireccionDestino.getText();
        boolean IdaYVuelta;
        if (RdBtnSi.isSelected()){
            IdaYVuelta=true;
        }else{
            IdaYVuelta=false;
        }
        java.util.Date FechaDeViaje = JdcFechaDeViaje.getDate();
        try {
            ////////////////////////////////////////////////////////
            Viaje via = new Viaje(idViaje, c, u, v, DireccionInicio, DireccionDestino, IdaYVuelta, FechaDeViaje);
            ////////////////////////////////////////////////////////
            boolean resp = new DaoViaje().Grabar(via);
            if (resp == true) {
                JOptionPane.showMessageDialog(null, "Grabo");
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
        Listar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error btn guardar: "+e.getMessage());
        } 
    }//GEN-LAST:event_btnGuardarActionPerformed

