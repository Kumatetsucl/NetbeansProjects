    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int fila=JTblListarViajes.getSelectedRow();
        int idViaje=Integer.parseInt(JTblListarViajes.getValueAt(fila, 0).toString());
        Viaje via = new DaoViaje().Buscar2(idViaje);
        if (via != null) {
            JOptionPane.showMessageDialog(null, "Existe Viaje ");
            txtIdViaje.setText(""+via.getId_viaje());
            cboRutConductor.setSelectedItem(via.getConductor().getRut());
            cboRutUsuario.setSelectedItem(via.getUsuario().getRut());
            cboVehiculo.setSelectedItem(via.getVehiculo().getChasisVehiculo());
            txtDireccionInicio.setText(via.getDireccionInicio());
            txtDireccionDestino.setText(via.getDireccionDestino());
            lblVehiculo.setText(""+via.getVehiculo().getIdVehiculo());
            lblIdConductor.setText(""+via.getConductor().getIdConductor());
            lblIdCliente.setText(""+via.getUsuario().getIdCliente());
            

            if (via.isIdaYVuelta()==true){
                RdBtnSi.setSelected(true);
            }else{
                RdBtnSi.setSelected(false);
            }
            JdcFechaDeViaje.setDate(via.getFechaDeViaje());
        } else {
            JOptionPane.showMessageDialog(null, "No Existe Viaje");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

