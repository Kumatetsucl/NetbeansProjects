    private void Listar() {
        DefaultTableModel modelo=new DefaultTableModel();
        JTblListarAutos.setModel(modelo);
        modelo.addColumn("Chasis");
        modelo.addColumn("Patente");
        modelo.addColumn("Año");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        ArrayList<Vehiculo> listado=new DaoVehiculo().Listar();
        for (Vehiculo item : listado) {
            Object[] fila=new Object[5];
            fila[0]=item.getChasisVehiculo();
            fila[1]=item.getPatenteVehiculo();
            fila[2]=item.getAnoFabricacion();
            fila[3]=item.getMarcaVehiculo();
            fila[4]=item.getModeloVehiculo();
            modelo.addRow(fila);
        }
    }
    
    private void ListarCbo(){
        
        cboMarcaVehiculo.setModel(new DefaultComboBoxModel(EnumMarca.values()));
        cboModeloVehiculo.setModel(new DefaultComboBoxModel(EnumModelo.values()));
    
    
        cboFiltroAno.addItem("Seleccione");
        ArrayList<Vehiculo> vehi= new ArrayList<>();
        DaoVehiculo rc=new DaoVehiculo();
        vehi=rc.Listar();
        for(int i=0;i<vehi.size();i++)
        {
            cboFiltroAno.addItem(""+vehi.get(i).getAnoFabricacion());
        }
    }
    
    public void ListarAno() {
        DefaultTableModel modelo = new DefaultTableModel();
        JTblListarAutos.setModel(modelo);
        modelo.addColumn("Chasis");
        modelo.addColumn("Patente");
        modelo.addColumn("Año");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        ArrayList<Vehiculo> listado=new DaoVehiculo().Listar();
        String AnoVeh = cboFiltroAno.getSelectedItem().toString();
        int An = Integer.parseInt(AnoVeh);
        for (Vehiculo item : listado) {
            if(item.getAnoFabricacion()==An){
                Object[] fila = new Object[5];
                fila[0]=item.getChasisVehiculo();
                fila[1]=item.getPatenteVehiculo();
                fila[2]=item.getAnoFabricacion();
                fila[3]=item.getMarcaVehiculo();
                fila[4]=item.getModeloVehiculo();
                modelo.addRow(fila);
            }
        }
    }

    
