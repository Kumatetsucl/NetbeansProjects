    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int idVehiculo = Integer.parseInt(txtIdVehiculo.getText());
        String chasisVehiculo = txtChasisVehiculo.getText();
        String patenteVehiculo= txtPatenteVehiculo.getText();
        int AnoFabricacion = Integer.parseInt(spnAnoFabricacion.getValue().toString());
        String marcaVehiculo= cboMarcaVehiculo.getSelectedItem().toString();
        String modeloVehiculo= cboModeloVehiculo.getSelectedItem().toString();

        try {
            ////////////////////////////////////////////////////////
            Vehiculo vehi = new Vehiculo(idVehiculo, chasisVehiculo, patenteVehiculo, AnoFabricacion, marcaVehiculo, modeloVehiculo);
            ////////////////////////////////////////////////////////
            boolean resp = new DaoVehiculo().Grabar(vehi);
            if (resp == true) {
                JOptionPane.showMessageDialog(null, "Grabo");
                Listar();
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error jfrm: "+e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

