    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int fila=JTblListarAutos.getSelectedRow();
        String chasis = JTblListarAutos.getValueAt(fila, 0).toString();
        Vehiculo vehi = new DaoVehiculo().Buscar(chasis);
        if (vehi != null) {    
            txtIdVehiculo.setText(""+vehi.getIdVehiculo());
            txtChasisVehiculo.setText(vehi.getChasisVehiculo());          
            txtPatenteVehiculo.setText(vehi.getPatenteVehiculo());          
            spnAnoFabricacion.setValue(vehi.getAnoFabricacion());
            cboMarcaVehiculo.setSelectedItem(EnumMarca.valueOf(vehi.getMarcaVehiculo()));
            cboModeloVehiculo.setSelectedItem(EnumModelo.valueOf(vehi.getModeloVehiculo()));
        } else {
            JOptionPane.showMessageDialog(null, "No Existe Vehiculo");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

