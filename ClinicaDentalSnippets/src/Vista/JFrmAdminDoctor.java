    private void BtnGrabarDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGrabarDoctorActionPerformed
        
        int id_doctor = Integer.parseInt(txtIdDoctor.getText());
        int num_rut = Integer.parseInt(txtRutDoctor.getText());
        int celular = Integer.parseInt(txtCelularDoctor.getText());
        int sueldo = Integer.parseInt(txtSueldoDoctor.getText());
        String especialidad = txtEspecialidadDoctor.getText();
        String nombre = txtNombreDoctor.getText();
        String direccion = txtDireccionDoctor.getText();
        
        
        
        
        try {
            ////////////////////////////////////////////////////////
            Doctor doc = new Doctor(id_doctor,especialidad, sueldo, nombre, num_rut, direccion, celular);
            ////////////////////////////////////////////////////////

            DaoDoctor dao = new DaoDoctor();
            boolean resp = dao.Grabar(doc);
            if (resp == true) {
                JOptionPane.showMessageDialog(null, "Grabo");
                Listar();
            } else {
                JOptionPane.showMessageDialog(null, "No Grabo");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_BtnGrabarDoctorActionPerformed

