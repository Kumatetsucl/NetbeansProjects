    public void ListarCbo(){
        cboEspecialidad.addItem("Seleccione");
        ArrayList<Especialidad> e= new ArrayList<>();
        DaoDoctor rg=new DaoDoctor();
        e=rg.ListarEspecialidad();
        for(int i=0;i<e.size();i++)
        {
          cboEspecialidad.addItem(e.get(i).getDescripcion());
        }
    }

    public void ListarEspecialidad() {
        DefaultTableModel modelo = new DefaultTableModel();
        JTblListaDoctor.setModel(modelo);
        modelo.addColumn("Id Doctor");
        modelo.addColumn("Rut Doctor");
        modelo.addColumn("Nombre Doctor");
        modelo.addColumn("Especialidad");
        modelo.addColumn("Celular Doctor");
        modelo.addColumn("Direccion Doctor");
        modelo.addColumn("Sueldo Doctor");
        ArrayList<Doctor> Listado = new DaoDoctor().Listar();
        String EspDoc = cboEspecialidad.getSelectedItem().toString();
        for (Doctor doctor : Listado) {
            if(doctor.getEspecialidad().equals(EspDoc)){
                Object[] fila = new Object[7];
                fila[0] = doctor.getId_doctor();
                fila[1] = doctor.getNum_rut();
                fila[2] = doctor.getNombre();
                fila[3] = doctor.getEspecialidad();
                fila[4] = doctor.getCelular();
                fila[5] = doctor.getDireccion();
                fila[6] = doctor.getSueldo();
                modelo.addRow(fila);
            }
        }
    }
    
    
    
    

