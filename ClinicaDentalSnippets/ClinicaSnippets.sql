-- QUERY PARA SABER LA FICHA DE LA ATENCION --
Select 
        ATE.ID_ATENCION AS "ID ATENCION",
        PAC.NOMBRE AS "NOMBRE PACIENTE",
        PAC.DIRECCION AS "DIRECCION PACIENTE",
        PAC.PREVISION AS "PREVISION PACIENTE",
        DOC.NOMBRE AS "NOMBRE DOCTOR",
        DOC.NUM_RUT AS "RUT DOCTOR",
        SERV.DESCRIPCION AS "DESCRIPCION SERVICIO",
        ATE.VALOR_ATENCION AS "VALOR ATENCION"
        from ATENCION ate 
        join doctor DOC
        on(ate.ID_DOCTOR = doc.ID_DOCTOR)
        JOIN PACIENTE PAC
        ON(ATE.ID_PACIENTE=PAC.ID_PACIENTE)
        JOIN ASISTENTE ASIS
        ON(ATE.id_asistente=ASIS.ID_ASISTENTE)
        JOIN SERVICIO SERV
        ON(ATE.ID_SERVICIO=SERV.iD_SERVICIO)
        
    GROUP BY ATE.ID_ATENCION,
            PAC.NOMBRE,
            PAC.DIRECCION,
            PAC.PREVISION,
            DOC.NOMBRE,
            DOC.NUM_RUT,
            SERV.DESCRIPCION,
            ATE.VALOR_ATENCION
;

