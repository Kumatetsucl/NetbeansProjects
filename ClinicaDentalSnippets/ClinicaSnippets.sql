---- CREATE TABLES ----
CREATE TABLE DOCTOR(
    ID_DOCTOR NUMERIC(20) NOT NULL,
    NOMBRE_DOCTOR VARCHAR2(80)NOT NULL,
    NUM_RUT_DOCTOR NUMERIC(9) NOT NULL,
    DIRECCION_DOCTOR VARCHAR2(30) NOT NULL,
    CELULAR_DOCTOR NUMERIC(9) NOT NULL,
    ESPECIALIDAD_DOCTOR VARCHAR2(20) NOT NULL,
    SUELDO_DOCTOR NUMERIC(8),
    CONSTRAINT PK_DOCTOR PRIMARY KEY (ID_DOCTOR)
);
CREATE TABLE ESPECIALIDAD (
    CODIGO_ESPECIALIDAD NUMBER(10) NOT NULL,
    DESCRIPCION_ESPECIALIDAD VARCHAR2(30) NOT NULL,
    CONSTRAINT PK_ESPECIALIDAD PRIMARY KEY (CODIGO_ESPECIALIDAD)
); 
CREATE TABLE ASISTENTE(
    ID_ASISTENTE NUMERIC(20) NOT NULL,
    NOMBRE_ASISTENTE VARCHAR2(80) NOT NULL,
    NUM_RUT_ASISTENTE NUMERIC(9) NOT NULL,
    DIRECCION_ASISTENTE VARCHAR2(30) NOT NULL,
    CELULAR_ASISTENTE NUMERIC(9) NOT NULL,
    SUELDO_ASISTENTE NUMERIC(8),
    CONSTRAINT PK_ASISTENTE PRIMARY KEY (ID_ASISTENTE)
);
CREATE TABLE PACIENTE(
    ID_PACIENTE NUMERIC(20) NOT NULL,
    NOMBRE_PACIENTE VARCHAR2(80) NOT NULL,
    NUM_RUT_PACIENTE NUMERIC(9) NOT NULL,
    DIRECCION_PACIENTE VARCHAR2(30) NOT NULL,
    CELULAR_PACIENTE NUMERIC(9) NOT NULL,
    PREVISION_PACIENTE VARCHAR2(20) NOT NULL,
    CONSTRAINT PK_PACIENTE PRIMARY KEY (ID_PACIENTE)
);
CREATE TABLE SERVICIO (
    ID_SERVICIO NUMERIC(20) NOT NULL,
    DESCRIPCION_SERVICIO VARCHAR2(255) NOT NULL,
    CONSTRAINT PK_SERVICIO PRIMARY KEY (ID_SERVICIO)
);
CREATE TABLE ATENCION (
    ID_ATENCION NUMERIC(9) NOT NULL,
    ID_DOCTOR NUMERIC(20) NOT NULL,
    ID_PACIENTE NUMERIC(20) NOT NULL,
    ID_ASISTENTE NUMERIC(20),
    ID_SERVICIO NUMERIC(20) NOT NULL,
    VALOR_ATENCION NUMERIC(7) NOT NULL,
    FECHA_ATENCION DATE NOT NULL,
    CONSTRAINT PK_ATENCION PRIMARY KEY (ID_ATENCION), 
    CONSTRAINT fk_ATENCION_DOCTOR FOREIGN KEY (ID_DOCTOR) REFERENCES DOCTOR(ID_DOCTOR),
    CONSTRAINT fk_ATENCION_PACIENTE FOREIGN KEY (ID_PACIENTE) REFERENCES PACIENTE(ID_PACIENTE),
    CONSTRAINT fk_ATENCION_ASISTENTE FOREIGN KEY (ID_ASISTENTE) REFERENCES ASISTENTE(ID_ASISTENTE),
    CONSTRAINT fk_ATENCION_SERVICIO FOREIGN KEY (ID_SERVICIO) REFERENCES SERVICIO(ID_SERVICIO)
);

